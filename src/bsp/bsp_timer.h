/*
 * bsp/bsp_timer.h
 * 功能：硬件定时器初始化；提供 1ms Tick 的基础能力或 Tick 回调入口。
 */

#pragma once

#ifndef __BSP_TIMER0_1MS_H__
#define __BSP_TIMER0_1MS_H__

#include <REG52.H>

/* =========================================================
   【模块定位】bsp/timer0_1ms：硬件 1ms 时基 + 回调分发器
   ---------------------------------------------------------
   你后续项目会有很多“需要每1ms推进”的模块：
     - tick（系统毫秒计数）
     - soft_timer（软定时器推进）
     - buzzer（蜂鸣器状态机）
     - keypad（按键去抖扫描）
   若每个模块都自己占用一个硬件定时器：51资源不够。
   所以工程做法是：
     1) Timer0 产生稳定的 1ms 中断（硬件负责“准时”）
     2) ISR 内只做极短的“回调分发”（软件负责“推进各模块”）
   ========================================================= */

/* =========================================================
   【时钟参数】
   本模块默认按 11.0592MHz 晶振计算 1ms 重装值。
   如果你的板子是 12MHz 或其它频率，需要调整重装值。
   ========================================================= */
#define T0_1MS_RELOAD_TH   0xFC
#define T0_1MS_RELOAD_TL   0x66
/* 解释：11.0592MHz 下，计数频率=Fosc/12=921600Hz
   1ms需要约922计数，65536-922=64614=0xFC66
*/

/* 1ms回调函数类型：在中断里被调用，必须极短 */
typedef void (*t0_hook_t)(void);

/* 初始化 Timer0 为 1ms 中断（仅配置硬件） */
void Timer0_1ms_Init(void);

/* 允许注册多个 1ms 回调：tick、soft_timer、buzzer等都挂这里 */
bit Timer0_1ms_RegisterHook(t0_hook_t hook);

/* 可选：清空所有hook（测试/重置时用） */
void Timer0_1ms_ClearHooks(void);

#endif